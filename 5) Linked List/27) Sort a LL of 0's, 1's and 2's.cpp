Node* sortElements(Node* head) {
    Node *temp = head;
    int zero=0, one=0, two=0;
    while(temp != NULL) {
        if(temp->data == 0) zero++;
        else if(temp->data == 1) one++;
        else two++;
        temp = temp->next;
    }
    temp = head;
    while(temp != NULL && zero-- > 0) {
        temp->data = 0;
        temp = temp->next;
    }
    while(temp != NULL && one-- > 0) {
        temp->data = 1;
        temp = temp->next;
    }
    while(temp != NULL && two-- > 0) {
        temp->data = 2;
        temp = temp->next;
    }
    return head;
}

void printElements() {
    if(start == NULL) {
        cout<<"List is Empty!";
    }
    else {
        Node *temp = start;
        cout<<"Data of the List is: ";
        while(temp != NULL) {
            cout<<temp->data<<" ";
            temp = temp->next;
        }
    }
}

